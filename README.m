# Free Game Finder

## Description
This app fetches real-time free game giveaways using the GamerPower API. Users can view a random giveaway and load new ones with a button.

## API Used
GamerPower API  
https://www.gamerpower.com/api-read

## How to Run
1. npm install
2. npm start

## Challenge
One challenge I faced was handling the API data since it returns an array. I solved this by selecting a random item from the array before displaying it.